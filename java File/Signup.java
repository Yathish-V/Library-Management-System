    private void btn_Create_AccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_Create_AccountActionPerformed
        // TODO add your handling code here:
        String username=jTextField1.getText();
        String name=jTextField2.getText();
        String password=jTextField3.getText();
        String answer=jTextField4.getText();
        
        if(username.equals(""))
           JOptionPane.showMessageDialog(null,"Please Enter username");
        else if(name.equals(""))
            JOptionPane.showMessageDialog(null,"Please Enter Name"); 
        else if(password.equals(""))
            JOptionPane.showMessageDialog(null,"Please Enter Password"); 
        else if(answer.equals(""))
         JOptionPane.showMessageDialog(null,"Please Enter Answer"); 
        else
        {
       
         try
        {
            String sql="Insert into Account(Username,Name,Password,Sec_Q,Answer) values(?,?,?,?,?) " ;
            
            pst=conn.prepareStatement(sql);
            pst.setString(1,jTextField1.getText());
            pst.setString(2,jTextField2.getText());
            pst.setString(3,jTextField3.getText());
            pst.setString(4,(String)jComboBox1.getSelectedItem());
            pst.setString(5,jTextField4.getText());
            pst.execute();
            JOptionPane.showMessageDialog(null,"New Account Created");
            
        }catch(HeadlessException | SQLException e)
        {
            JOptionPane.showMessageDialog(null,"Aready exist Please enter unique Id");

        }
       }
    }//GEN-LAST:event_btn_Create_AccountActionPerformed

